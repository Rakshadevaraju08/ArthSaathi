export function getTranslations(language: Lang): Record<string, string> {
  const activeTranslations = TRANSLATIONS[language as string] || TRANSLATIONS.English;
  return Object.assign({}, TRANSLATIONS.English, activeTranslations);
}

export function useTranslations() {
  const language = useStore((state) => state.language);
  return useMemo(() => getTranslations(language), [language]);
}
